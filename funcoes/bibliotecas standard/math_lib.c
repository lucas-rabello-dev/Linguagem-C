// math.h
#include <stdio.h>
#include <math.h>

int main() {
    
    // seno, cosseno e tangente kkk
    double a = cos(3.3);
    double b = sin(4.4);
    double c = tan(5.5);
    // ambos recebem um double como parametro e retornam um double
    
    printf("%.2f, %.2f, %.2f\n", a, b, c);
    
    // logaritimo, ambos recebem um double e retornam um double tbm
    log(2.2);
    // log na base 10
    log10(2.3);
    
    // raiz quadrada, recebe um double e retorna double
    sqrt(30.9);
    
    
    // retorna o inteiro maior ou igual a x, recebe double
    ceil(4.4);
    
    
    
    return 0;
}